package main.entity;

public class Cover {
    private Dish dish;
    private String note;

    public Cover() {
        this.dish = dish;
        this.note = note;
    }

    public Dish getDish() {
        return dish;
    }

    public void setDish(Dish dish) {
        this.dish = dish;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
}
